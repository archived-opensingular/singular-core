package org.opensingular.form.wicket.mapper.attachment.upload;

import java.io.Serializable;
import java.util.UUID;

/**
 * Classe para encapsular a key de attachment
 */
public class AttachmentKey implements Serializable {

    private String key;

    public AttachmentKey(String key) {
        this.key = key;
    }

    public String asString() {
        return key;
    }

    @Override
    public String toString() {
        return key;
    }

    @Override
    public boolean equals(Object o) {

        if (this == o) {
            return true;
        }

        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        AttachmentKey that = (AttachmentKey) o;

        return key != null ? key.equals(that.key) : that.key == null;
    }

    @Override
    public int hashCode() {
        return key != null ? key.hashCode() : 0;
    }

}